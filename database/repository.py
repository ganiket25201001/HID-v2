class AlertRepository:
    """CRUD operations for the ``SystemAlert`` table."""

    @classmethod
    def create_alert(
        cls,
        session: Session,
        *,
        title: str,
        message: str,
        severity: AlertSeverity | str = AlertSeverity.INFO,
        category: AlertCategory | str = AlertCategory.SYSTEM,
        device_event_id: Optional[int] = None,
        is_simulated: bool = False,
        source: Optional[str] = None,
    ) -> SystemAlert:
        """Create a new ``SystemAlert`` row.

        Parameters
        ----------
        session:
            Active SQLAlchemy session.
        title:
            Short alert title shown in the notification area.
        message:
            Full alert body text.
        severity:
            ``AlertSeverity`` level.
        category:
            ``AlertCategory`` functional area.
        device_event_id:
            Optional FK to a related ``DeviceEvent``.
        is_simulated:
            True in simulation mode.
        source:
            Name of the subsystem raising this alert.

        Returns
        -------
        SystemAlert
            The newly created and flushed ``SystemAlert`` row.
        """
        sev_val: str = (
            severity.value if isinstance(severity, AlertSeverity) else severity
        )
        cat_val: str = (
            category.value if isinstance(category, AlertCategory) else category
        )

        alert = SystemAlert(
            title=title,
            message=message,
            severity=sev_val,
            category=cat_val,
            device_event_id=device_event_id,
            is_simulated=is_simulated,
            source=source,
            timestamp=datetime.now(timezone.utc),
        )
        session.add(alert)
        session.flush()
        return alert

    @classmethod
    def get_unread_alerts(
        cls,
        limit: int = 50,
        session: Optional[Session] = None,
    ) -> list[SystemAlert]:
        """Return unread alerts, newest first.

        Parameters
        ----------
        limit:
            Maximum number of rows to return.
        session:
            Optional existing session.
        """
        stmt = (
            select(SystemAlert)
            .where(
                SystemAlert.is_read.is_(False),
                SystemAlert.is_dismissed.is_(False),
            )
            .order_by(desc(SystemAlert.timestamp))
            .limit(limit)
        )

        if session is not None:
            return list(session.scalars(stmt).all())

        with get_db() as s:
            rows = list(s.scalars(stmt).all())
            for row in rows:
                s.expunge(row)
            return rows

    @classmethod
    def mark_as_read(
        cls,
        session: Session,
        alert_id: int,
    ) -> Optional[SystemAlert]:
        """Mark a single alert as read.

        Parameters
        ----------
        session:
            Active session.
        alert_id:
            PK of the alert.

        Returns
        -------
        SystemAlert or None
        """
        alert = session.get(SystemAlert, alert_id)
        if alert is None:
            return None
        alert.is_read = True
        session.flush()
        return alert

    @classmethod
    def mark_all_as_read(cls, session: Session) -> int:
        """Mark all unread, non-dismissed alerts as read.

        Parameters
        ----------
        session:
            Active session.

        Returns
        -------
        int
            Number of alerts updated.
        """
        stmt = (
            select(SystemAlert)
            .where(
                SystemAlert.is_read.is_(False),
                SystemAlert.is_dismissed.is_(False),
            )
        )
        alerts: list[SystemAlert] = list(session.scalars(stmt).all())
        for alert in alerts:
            alert.is_read = True
        session.flush()
        return len(alerts)

    @classmethod
    def dismiss_alert(
        cls,
        session: Session,
        alert_id: int,
    ) -> Optional[SystemAlert]:
        """Dismiss a single alert from the UI notification centre.

        Parameters
        ----------
        session:
            Active session.
        alert_id:
            PK of the alert.
        """
        alert = session.get(SystemAlert, alert_id)
        if alert is None:
            return None
        alert.is_dismissed = True
        session.flush()
        return alert

    @classmethod
    def get_recent_alerts(
        cls,
        limit: int = 20,
        session: Optional[Session] = None,
    ) -> list[SystemAlert]:
        """Return recent alerts (read and unread), newest first.

        Parameters
        ----------
        limit:
            Maximum rows to return.
        session:
            Optional existing session.
        """
        stmt = (
            select(SystemAlert)
            .order_by(desc(SystemAlert.timestamp))
            .limit(limit)
        )

        if session is not None:
            return list(session.scalars(stmt).all())

        with get_db() as s:
            rows = list(s.scalars(stmt).all())
            for row in rows:
                s.expunge(row)
            return rows

    @classmethod
    def get_unread_count(cls, session: Optional[Session] = None) -> int:
        """Return the count of unread, non-dismissed alerts.

        Parameters
        ----------
        session:
            Optional existing session.
        """
        stmt = select(func.count()).select_from(SystemAlert).where(
            SystemAlert.is_read.is_(False),
            SystemAlert.is_dismissed.is_(False),
        )

        if session is not None:
            return session.scalar(stmt) or 0

        with get_db() as s:
            return s.scalar(stmt) or 0
