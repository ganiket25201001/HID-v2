"""
hid_shield.database.repository
================================
Repository (CRUD helper) classes for each ORM model.

Design decisions
----------------
* All methods are ``@classmethod`` so the classes can be used without
  instantiation, keeping call-sites concise:
  ``DeviceRepository.create_event(session, ...)``.
* Every method that writes to the database accepts an explicit ``Session``
  argument so the caller controls transaction scope (typically via ``get_db``).
* Read methods accept an optional ``session`` parameter — when one is
  provided it is reused; otherwise the method opens its own via ``get_db()``.
* No global state is held; the classes are pure stateless helper namespaces.
"""

from __future__ import annotations

from datetime import datetime, timezone
from typing import Optional

from sqlalchemy import desc, func, select
from sqlalchemy.orm import Session

from database.db import get_db
from database.models import (
    AlertCategory,
    AlertSeverity,
    DeviceEvent,
    DeviceType,
    FileScanResult,
    PolicyAction,
    RiskLevel,
    SystemAlert,
    UserAction,
)


