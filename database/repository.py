class FileScanRepository:
    """CRUD operations for the ``FileScanResult`` table."""

    @classmethod
    def log_file_scan(
        cls,
        session: Session,
        *,
        device_event_id: int,
        file_path: str,
        file_name: str,
        file_size_bytes: Optional[int] = None,
        sha256_hash: Optional[str] = None,
        md5_hash: Optional[str] = None,
        is_malicious: bool = False,
        threat_name: Optional[str] = None,
        scan_engine: Optional[str] = None,
        risk_level: RiskLevel | str = RiskLevel.SAFE,
        is_simulated: bool = False,
        notes: Optional[str] = None,
    ) -> FileScanResult:
        """Create and persist a ``FileScanResult`` row.

        Parameters
        ----------
        session:
            Active SQLAlchemy session.
        device_event_id:
            FK to the parent ``DeviceEvent``.
        file_path:
            Full path on the device.
        file_name:
            Basename of the file.
        file_size_bytes:
            File size in bytes (optional).
        sha256_hash:
            SHA-256 hex digest.
        md5_hash:
            MD5 hex digest.
        is_malicious:
            True if the hash matched a threat signature.
        threat_name:
            Threat family name if malicious.
        scan_engine:
            Identifier and version string of the scan engine.
        risk_level:
            Risk level for this file.
        is_simulated:
            True in simulation mode.
        notes:
            Additional notes.

        Returns
        -------
        FileScanResult
            The newly created and flushed row.
        """
        risk_val: str = (
            risk_level.value if isinstance(risk_level, RiskLevel) else risk_level
        )

        result = FileScanResult(
            device_event_id=device_event_id,
            file_path=file_path,
            file_name=file_name,
            file_size_bytes=file_size_bytes,
            sha256_hash=sha256_hash,
            md5_hash=md5_hash,
            is_malicious=is_malicious,
            threat_name=threat_name,
            scan_engine=scan_engine,
            risk_level=risk_val,
            is_simulated=is_simulated,
            notes=notes,
            timestamp=datetime.now(timezone.utc),
        )
        session.add(result)
        session.flush()
        return result

    @classmethod
    def get_scans_for_event(
        cls,
        device_event_id: int,
        session: Optional[Session] = None,
    ) -> list[FileScanResult]:
        """Return all scan results linked to a device event.

        Parameters
        ----------
        device_event_id:
            PK of the parent ``DeviceEvent``.
        session:
            Optional existing session.
        """
        stmt = (
            select(FileScanResult)
            .where(FileScanResult.device_event_id == device_event_id)
            .order_by(desc(FileScanResult.timestamp))
        )

        if session is not None:
            return list(session.scalars(stmt).all())

        with get_db() as s:
            rows = list(s.scalars(stmt).all())
            for row in rows:
                s.expunge(row)
            return rows

    @classmethod
    def get_malicious_scans(
        cls,
        limit: int = 50,
        session: Optional[Session] = None,
    ) -> list[FileScanResult]:
        """Return the most recent malicious file scan results.

        Parameters
        ----------
        limit:
            Maximum rows to return.
        session:
            Optional existing session.
        """
        stmt = (
            select(FileScanResult)
            .where(FileScanResult.is_malicious.is_(True))
            .order_by(desc(FileScanResult.timestamp))
            .limit(limit)
        )

        if session is not None:
            return list(session.scalars(stmt).all())

        with get_db() as s:
            rows = list(s.scalars(stmt).all())
            for row in rows:
                s.expunge(row)
            return rows

    @classmethod
    def find_by_hash(
        cls,
        sha256_hash: str,
        session: Optional[Session] = None,
    ) -> list[FileScanResult]:
        """Look up all scan results that share the given SHA-256 hash.

        Useful for checking whether a specific file hash has been seen before.

        Parameters
        ----------
        sha256_hash:
            64-character hex digest to search for.
        session:
            Optional existing session.
        """
        stmt = select(FileScanResult).where(
            FileScanResult.sha256_hash == sha256_hash
        )

        if session is not None:
            return list(session.scalars(stmt).all())

        with get_db() as s:
            rows = list(s.scalars(stmt).all())
            for row in rows:
                s.expunge(row)
            return rows


