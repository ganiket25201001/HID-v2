class UserActionRepository:
    """CRUD operations for the ``UserAction`` audit-log table."""

    @classmethod
    def log_action(
        cls,
        session: Session,
        *,
        device_event_id: int,
        action: PolicyAction | str,
        operator_id: Optional[str] = None,
        reason: Optional[str] = None,
        was_override: bool = False,
        previous_action: Optional[PolicyAction | str] = None,
        ip_address: Optional[str] = None,
        notes: Optional[str] = None,
    ) -> UserAction:
        """Record an operator action against a device event.

        Parameters
        ----------
        session:
            Active SQLAlchemy session.
        device_event_id:
            FK to the affected ``DeviceEvent``.
        action:
            The policy action chosen by the operator.
        operator_id:
            Operator username or PIN-hash prefix.
        reason:
            Justification text.
        was_override:
            True if this action overrides an automatic decision.
        previous_action:
            The automatic action that was overridden.
        ip_address:
            Source IP for remote admin audit trail.
        notes:
            Free-text notes.

        Returns
        -------
        UserAction
            The newly created and flushed ``UserAction`` row.
        """
        action_val: str = (
            action.value if isinstance(action, PolicyAction) else action
        )
        prev_val: Optional[str] = None
        if previous_action is not None:
            prev_val = (
                previous_action.value
                if isinstance(previous_action, PolicyAction)
                else previous_action
            )

        user_action = UserAction(
            device_event_id=device_event_id,
            action=action_val,
            operator_id=operator_id,
            reason=reason,
            was_override=was_override,
            previous_action=prev_val,
            ip_address=ip_address,
            notes=notes,
            timestamp=datetime.now(timezone.utc),
        )
        session.add(user_action)
        session.flush()
        return user_action

    @classmethod
    def get_actions_for_event(
        cls,
        device_event_id: int,
        session: Optional[Session] = None,
    ) -> list[UserAction]:
        """Return all user actions linked to a given device event.

        Parameters
        ----------
        device_event_id:
            PK of the parent ``DeviceEvent``.
        session:
            Optional existing session.
        """
        stmt = (
            select(UserAction)
            .where(UserAction.device_event_id == device_event_id)
            .order_by(desc(UserAction.timestamp))
        )

        if session is not None:
            return list(session.scalars(stmt).all())

        with get_db() as s:
            rows = list(s.scalars(stmt).all())
            for row in rows:
                s.expunge(row)
            return rows

    @classmethod
    def get_recent_actions(
        cls,
        limit: int = 20,
        session: Optional[Session] = None,
    ) -> list[UserAction]:
        """Return the most recent user actions across all device events.

        Parameters
        ----------
        limit:
            Maximum number of rows to return.
        session:
            Optional existing session.
        """
        stmt = (
            select(UserAction)
            .order_by(desc(UserAction.timestamp))
            .limit(limit)
        )

        if session is not None:
            return list(session.scalars(stmt).all())

        with get_db() as s:
            rows = list(s.scalars(stmt).all())
            for row in rows:
                s.expunge(row)
            return rows


