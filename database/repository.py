class DeviceRepository:
    """CRUD operations for the ``DeviceEvent`` table.

    All write methods require an explicit ``Session`` so the caller can group
    multiple operations in a single transaction.

    Examples
    --------
    .. code-block:: python

        from database.db import get_db
        from database.repository import DeviceRepository

        with get_db() as session:
            event = DeviceRepository.create_event(
                session,
                device_name="SuspiciousKB",
                risk_level=RiskLevel.HIGH,
                action_taken=PolicyAction.BLOCK,
            )
    """

    @classmethod
    def create_event(
        cls,
        session: Session,
        *,
        device_name: str = "Unknown Device",
        vendor_id: Optional[str] = None,
        product_id: Optional[str] = None,
        serial: Optional[str] = None,
        manufacturer: Optional[str] = None,
        device_type: DeviceType | str = DeviceType.UNKNOWN,
        risk_level: RiskLevel | str = RiskLevel.LOW,
        entropy_score: Optional[float] = None,
        keystroke_rate: Optional[float] = None,
        action_taken: PolicyAction | str = PolicyAction.PROMPT,
        is_simulated: bool = False,
        notes: Optional[str] = None,
    ) -> DeviceEvent:
        """Create and persist a new ``DeviceEvent`` row.

        Parameters
        ----------
        session:
            An active SQLAlchemy session (from ``get_db()``).
        device_name:
            Human-readable name of the device.
        vendor_id, product_id:
            USB VID / PID as 4-hex-digit strings.
        serial:
            USB serial number string.
        manufacturer:
            USB manufacturer string.
        device_type:
            ``DeviceType`` enum value or its string equivalent.
        risk_level:
            ``RiskLevel`` enum value or its string equivalent.
        entropy_score:
            Keystroke-entropy score in [0.0, 1.0].
        keystroke_rate:
            Observed keystrokes per second.
        action_taken:
            ``PolicyAction`` applied to this event.
        is_simulated:
            True when generated by the simulation engine.
        notes:
            Free-text analyst notes.

        Returns
        -------
        DeviceEvent
            The newly created (and flushed) ``DeviceEvent`` instance.
        """
        # Normalise enum values — accept both enum member and raw string
        device_type_val: str = (
            device_type.value if isinstance(device_type, DeviceType) else device_type
        )
        risk_level_val: str = (
            risk_level.value if isinstance(risk_level, RiskLevel) else risk_level
        )
        action_val: str = (
            action_taken.value if isinstance(action_taken, PolicyAction) else action_taken
        )

        event = DeviceEvent(
            device_name=device_name,
            vendor_id=vendor_id,
            product_id=product_id,
            serial=serial,
            manufacturer=manufacturer,
            device_type=device_type_val,
            risk_level=risk_level_val,
            entropy_score=entropy_score,
            keystroke_rate=keystroke_rate,
            action_taken=action_val,
            is_simulated=is_simulated,
            notes=notes,
            timestamp=datetime.now(timezone.utc),
        )
        session.add(event)
        session.flush()   # Populate auto-generated ``id`` without committing
        return event

    @classmethod
    def get_event_by_id(
        cls,
        event_id: int,
        session: Optional[Session] = None,
    ) -> Optional[DeviceEvent]:
        """Fetch a single ``DeviceEvent`` by primary key.

        Parameters
        ----------
        event_id:
            The integer primary key of the event.
        session:
            Optional existing session; opens a new one if not provided.

        Returns
        -------
        DeviceEvent or None
        """
        if session is not None:
            return session.get(DeviceEvent, event_id)

        with get_db() as s:
            return s.get(DeviceEvent, event_id)

    @classmethod
    def get_recent_events(
        cls,
        limit: int = 50,
        session: Optional[Session] = None,
    ) -> list[DeviceEvent]:
        """Return the most recent ``DeviceEvent`` rows, newest first.

        Parameters
        ----------
        limit:
            Maximum number of rows to return (default 50).
        session:
            Optional existing session.

        Returns
        -------
        list[DeviceEvent]
        """
        stmt = (
            select(DeviceEvent)
            .order_by(desc(DeviceEvent.timestamp))
            .limit(limit)
        )

        if session is not None:
            return list(session.scalars(stmt).all())

        with get_db() as s:
            # Expunge objects so they can be used after the session closes
            rows = list(s.scalars(stmt).all())
            for row in rows:
                s.expunge(row)
            return rows

    @classmethod
    def get_events_by_risk(
        cls,
        risk_level: RiskLevel | str,
        limit: int = 100,
        session: Optional[Session] = None,
    ) -> list[DeviceEvent]:
        """Return events filtered by ``risk_level``, newest first.

        Parameters
        ----------
        risk_level:
            Filter by this risk level.
        limit:
            Maximum rows to return.
        session:
            Optional existing session.
        """
        risk_val: str = (
            risk_level.value if isinstance(risk_level, RiskLevel) else risk_level
        )
        stmt = (
            select(DeviceEvent)
            .where(DeviceEvent.risk_level == risk_val)
            .order_by(desc(DeviceEvent.timestamp))
            .limit(limit)
        )

        if session is not None:
            return list(session.scalars(stmt).all())

        with get_db() as s:
            rows = list(s.scalars(stmt).all())
            for row in rows:
                s.expunge(row)
            return rows

    @classmethod
    def get_high_risk_count(cls, session: Optional[Session] = None) -> int:
        """Return the count of HIGH or CRITICAL risk events.

        Parameters
        ----------
        session:
            Optional existing session.
        """
        stmt = select(func.count()).select_from(DeviceEvent).where(
            DeviceEvent.risk_level.in_([RiskLevel.HIGH.value, RiskLevel.CRITICAL.value])
        )

        if session is not None:
            return session.scalar(stmt) or 0

        with get_db() as s:
            return s.scalar(stmt) or 0

    @classmethod
    def update_action(
        cls,
        session: Session,
        event_id: int,
        new_action: PolicyAction | str,
    ) -> Optional[DeviceEvent]:
        """Update the ``action_taken`` field on an existing event.

        Parameters
        ----------
        session:
            Active session.
        event_id:
            PK of the event to update.
        new_action:
            New ``PolicyAction`` to set.

        Returns
        -------
        DeviceEvent or None
            The updated event, or ``None`` if not found.
        """
        event = session.get(DeviceEvent, event_id)
        if event is None:
            return None

        action_val: str = (
            new_action.value if isinstance(new_action, PolicyAction) else new_action
        )
        event.action_taken = action_val
        session.flush()
        return event

    @classmethod
    def delete_event(cls, session: Session, event_id: int) -> bool:
        """Delete a ``DeviceEvent`` (and its cascaded children) by PK.

        Parameters
        ----------
        session:
            Active session.
        event_id:
            PK of the event to delete.

        Returns
        -------
        bool
            ``True`` if the row was found and deleted, ``False`` otherwise.
        """
        event = session.get(DeviceEvent, event_id)
        if event is None:
            return False
        session.delete(event)
        session.flush()
        return True


